#include<iostream>
using namespace std;

int main(){
    cout<<"this is me";
    int a = 2, b = 5;
    cout<<"the value of A + B is:"<<a+b<<endl;
    cout<<"the value of A - B is:"<<a-b<<endl;
    cout<<"the value of A * B is:"<<a*b<<endl;
    cout<<"the value of A / B is:"<<a/b<<endl;
    cout<<"the value of A % B is:"<<a%b<<endl;
    cout<<"the value of A ++ is:"<<a++<<endl;
    cout<<"the value of A -- is:"<<a--<<endl;
    cout<<"the value of -- A is:"<<--a<<endl;
    cout<<"the value of ++ A is:"<<++a<<endl;
    return 0;
}










